// objects for name, age, location
// increase age by 1 and print it again

let person = {
    name: 'Andreas',
    age: 25,
    location: 'Taichung'
}

console.log(`My name is ${person.name}, I am ${person.age} years old and I live in ${person.location}`)

person.age = person.age + 1

console.log(`My name is ${person.name}, I am ${person.age} years old and I live in ${person.location}`)

